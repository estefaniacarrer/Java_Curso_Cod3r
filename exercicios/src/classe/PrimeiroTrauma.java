package classe;

public class PrimeiroTrauma {
	
	int a = 3; // não alterar
	static int b = 4; //segunda opção
	
	public static void main(String[] args) {
		
		PrimeiroTrauma p = new PrimeiroTrauma();
		
		System.out.println(p.a);
		System.out.println(b); // tornando o atributo em static não precisa criar a instância
		
		
	}

}
